import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import { XMLParser } from 'fast-xml-parser';

const parser = new XMLParser({
  ignoreAttributes: false,
  attributeNamePrefix: '',
  parseTagValue: false,
  trimValues: false,
});

const toArray = (value) => {
  if (value === undefined) {
    return [];
  }
  return Array.isArray(value) ? value : [value];
};

const asRecord = (value) => (value && typeof value === 'object' ? value : {});

const parsePorts = (value) =>
  toArray(value)
    .map((entry) => {
      const attrs = asRecord(entry);
      const name = attrs.name ? String(attrs.name) : '';
      if (!name) {
        return null;
      }
      return {
        name,
        type: attrs.type ? String(attrs.type) : undefined,
      };
    })
    .filter((entry) => entry !== null);

const parseNodeDef = (entry) => {
  const attrs = asRecord(entry);
  const category = attrs.node ? String(attrs.node).trim() : '';
  if (!category) {
    return null;
  }

  return {
    category,
    nodeDefName: attrs.name ? String(attrs.name) : undefined,
    type: attrs.type ? String(attrs.type) : undefined,
    inputs: parsePorts(attrs.input),
    outputs: parsePorts(attrs.output),
    parameters: parsePorts(attrs.parameter),
  };
};

const listMtlxFiles = async (directory) => {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await listMtlxFiles(fullPath)));
      continue;
    }
    if (entry.isFile() && entry.name.endsWith('.mtlx')) {
      files.push(fullPath);
    }
  }
  return files;
};

const pickSlimSpec = (current, candidate) => {
  if (!current) {
    return candidate;
  }

  const currentSurfaceShader = (current.nodeDefName || '').endsWith('_surfaceshader');
  const candidateSurfaceShader = (candidate.nodeDefName || '').endsWith('_surfaceshader');
  if (candidateSurfaceShader && !currentSurfaceShader) {
    return candidate;
  }
  if (!candidateSurfaceShader && currentSurfaceShader) {
    return current;
  }

  const currentScore = current.inputs.length + current.outputs.length + current.parameters.length;
  const candidateScore = candidate.inputs.length + candidate.outputs.length + candidate.parameters.length;
  if (candidateScore > currentScore) {
    return candidate;
  }

  return current;
};

const generateRegistry = async (librariesDir) => {
  const files = await listMtlxFiles(librariesDir);
  const byCategory = new Map();

  for (const filePath of files) {
    const xml = await readFile(filePath, 'utf8');
    const parsed = parser.parse(xml);
    const root = asRecord(parsed.materialx);
    for (const nodeDef of toArray(root.nodedef)) {
      const spec = parseNodeDef(nodeDef);
      if (!spec) {
        continue;
      }
      byCategory.set(spec.category, pickSlimSpec(byCategory.get(spec.category), spec));
    }
  }

  const categories = [...byCategory.keys()].toSorted((a, b) => a.localeCompare(b));
  const specs = categories.map((category) => byCategory.get(category));
  return { categories, specs };
};

const toSource = ({ categories, specs }) => {
  const body = JSON.stringify(specs, null, 2);
  const categoryBody = JSON.stringify(categories, null, 2);
  return `// Generated by scripts/generate-node-registry.mjs
// Source: MaterialX libraries nodedefs.
export const generatedSlimNodeRegistry = ${body};

export const generatedSlimNodeCategories = ${categoryBody};
`;
};

const main = async () => {
  const packageRoot = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
  const librariesDir = process.env.MATERIALX_LIBRARIES_DIR
    ? path.resolve(process.env.MATERIALX_LIBRARIES_DIR)
    : path.resolve(packageRoot, '../../../MaterialX/libraries');
  const outputPath = path.resolve(packageRoot, 'viewer/src/vendor/materialx/generated/MaterialXNodeRegistry.generated.js');

  const registry = await generateRegistry(librariesDir);
  await writeFile(outputPath, toSource(registry), 'utf8');
  process.stdout.write(
    `Generated slim registry with ${registry.categories.length} categories from ${librariesDir}\n`,
  );
};

main().catch((error) => {
  process.stderr.write(`${error instanceof Error ? error.stack : String(error)}\n`);
  process.exit(1);
});
